export interface CssSummary {
  bytes: number
  selectors: string[]
  hasSupports: boolean
  hasHoverPseudo: boolean
  hasTailwindBanner: boolean
  hasSystemDarkModeMedia: boolean
  hasManualDarkModeSelector: boolean
  hasUnsupportedThemeAttributeSelector: boolean
  hasUnsupportedThemeComplexSelector: boolean
  hasWeappEscapedArbitrarySelector: boolean
  hasRawArbitrarySelector: boolean
}

export interface CompareReportItem {
  name: string
  fixture: 'demo'
  platform: string
  allowedPlatforms: string[]
  cssFile: string
  cssFiles: string[]
  status: 'passed'
  generator: CssSummary
}

export interface FailedCompareReportItem {
  name: string
  fixture: 'demo'
  platform: string
  allowedPlatforms: string[]
  cssFile: string
  cssFiles: string[]
  status: 'failed'
  error: string
}

export type AppsGeneratorCompareReportItem = CompareReportItem | FailedCompareReportItem

function formatBoolean(value: boolean) {
  return value ? 'yes' : 'no'
}

function formatChineseBoolean(value: boolean) {
  return value ? 'µś»' : 'ÕÉ”'
}

function formatSelectorList(selectors: string[]) {
  if (selectors.length === 0) {
    return '-'
  }
  return selectors.slice(0, 20).map(selector => `\`${selector.replaceAll('|', '\\|')}\``).join(', ')
}

function formatCssFiles(item: AppsGeneratorCompareReportItem) {
  if (item.cssFiles.length <= 1) {
    return `\`${item.cssFile}\``
  }
  return `\`${item.cssFile}\` (+${item.cssFiles.length - 1})`
}

function formatPlatformList(platforms: string[]) {
  if (platforms.length === 0) {
    return '-'
  }
  return platforms.map(platform => `\`${platform.replaceAll('|', '\\|')}\``).join(', ')
}

